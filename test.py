def add_numbers(a, b):
    """
    Compute the sum of two values.
    
    Parameters:
        a: The first addend.
        b: The second addend.
    
    Returns:
        The result of adding `a` and `b`.
    """
    return a + b

# Example usage
result = add_numbers(5, 3)
print("The sum is:", result)
